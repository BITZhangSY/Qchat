package com.bit_zt.proj_socket.View;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

/**
 * Created by bit_zt on 15/11/8.
 */
public class Adapter_mainViewPager extends FragmentPagerAdapter {

    List<Fragment> fragmentList;


    public Adapter_mainViewPager(FragmentManager fm, List<Fragment> fragmentList) {
        super(fm);
        this.fragmentList = fragmentList;
    }

    @Override
    public Fragment getItem(int position) {
        return fragmentList.get(position);
    }

    @Override
    public int getCount() {
        return fragmentList.size();
    }
}
